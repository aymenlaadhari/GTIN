    private void populateJtable(List<Status> statuses){
        tableModel.setRowCount(0);
        statuses.stream().forEach(cnsmr->{
        addToTable(cnsmr);
        tableModel.addRow(rowDataMuster);    
        });
        
    }
    
    private void addToTable(Status status){
        rowDataMuster[0] = status.getCode();
        rowDataMuster[1] = status.getText();
    }
