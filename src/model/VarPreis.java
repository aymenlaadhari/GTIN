public class VarPreis {
    private String zeile, varNummer, varText, kdPreis, vkPreis;

    public String getZeile() {
        return zeile;
    }

    public void setZeile(String zeile) {
        this.zeile = zeile;
    }

    public String getVarNummer() {
        return varNummer;
    }

    public void setVarNummer(String varNummer) {
        this.varNummer = varNummer;
    }

    public String getVarText() {
        return varText;
    }

    public void setVarText(String varText) {
        this.varText = varText;
    }

    public String getKdPreis() {
        return kdPreis;
    }

    public void setKdPreis(String kdPreis) {
        this.kdPreis = kdPreis;
    }

    public String getVkPreis() {
        return vkPreis;
    }

    public void setVkPreis(String vkPreis) {
        this.vkPreis = vkPreis;
    }
    
}
